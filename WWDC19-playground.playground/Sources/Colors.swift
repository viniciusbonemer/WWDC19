import class UIKit.UIColor

public
enum Colors {
    
    public static
    var darkGray: UIColor {
        return UIColor(red: 118/225, green: 118/225, blue: 118/225, alpha: 1)
    }
    
    public static
    var gray: UIColor {
        return UIColor(red: 158/225, green: 161/225, blue: 165/225, alpha: 1)
    }
    
    public static
    var lightGray: UIColor {
        return UIColor(red: 185/225, green: 188/225, blue: 185/225, alpha: 1)
    }
    
    public static
    var black: UIColor {
        return UIColor(red: 75/225, green: 75/225, blue: 75/225, alpha: 1)
    }
    
    public static
    var white: UIColor {
        return .white
    }
    
    public static
    var blue: UIColor {
        return UIColor(red: 68/225, green: 53/225, blue: 217/225, alpha: 1)
    }
    
    public static
    var pink: UIColor {
        return UIColor(red: 206/225, green: 159/225, blue: 252/225, alpha: 1)
    }
    
    public static
    var purple: UIColor {
        return UIColor(red: 115/225, green: 103/225, blue: 240/225, alpha: 1)
    }
    
}

